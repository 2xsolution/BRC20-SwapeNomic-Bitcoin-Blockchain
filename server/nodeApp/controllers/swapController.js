const TOKEN_IN_ADDRESS = "0x0000000000000000000000000000000000001010"; // Replace with the address of the MATIC token
const TOKEN_OUT_ADDRESS = "0x44Bc761E0B58Aa6727202eBd2B636DC924dA9f1a"; // Replace with the address of the token you want to receive
const AMOUNT_IN = ethers.utils.parseUnits("1", "18"); // Replace 'AMOUNT_IN' and 'TOKEN_DECIMALS'
const AMOUNT_OUT_MIN = ethers.utils.parseUnits("0.00258", "18"); // Replace 'AMOUNT_OUT_MIN' and 'TOKEN_DECIMALS'
const RECEIVER_ADDRESS = "0xd0A2A25C75307078493C5097367BD85b0E78b1a4"; // Replace with the address that will receive the swapped tokens
const DEADLINE = Math.floor(Date.now() / 1000) + 3600; // Set the deadline (e.g., 1 hour from now)

async function swapTokens() {
  try {
    const provider = new ethers.providers.JsonRpcProvider(
      MATIC_NETWORK_RPC_URL
    );
    const wallet = new ethers.Wallet(PRIVATE_KEY, provider);
    const contract = new ethers.Contract(
      CONTRACT_ADDRESS,
      CONTRACT_ABI,
      wallet
    );

    const path = [TOKEN_IN_ADDRESS, TOKEN_OUT_ADDRESS];

    const tx = await contract.swapExactTokensForTokens(
      AMOUNT_IN,
      AMOUNT_OUT_MIN,
      path,
      RECEIVER_ADDRESS,
      DEADLINE,
      { gasLimit: 20000000 } // Adjust the gas limit as needed
    );

    console.log("Transaction hash:", tx.hash);

    // Wait for the transaction to be mined
    await tx.wait();

    console.log("Transaction confirmed");
  } catch (error) {
    console.error("Error:", error);
  }
}

const swap = async function performSwap(req, res) {
  swapTokens();
};

